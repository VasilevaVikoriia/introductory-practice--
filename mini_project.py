# Вариант 10: Анализ прайс-листа автомобилей
# Раздел: Бизнес-аналитика для автосалона
# Задача: оценка ценового портфеля, расчёт маржинальности,
#         прогноз выручки при изменении ценовой политики
# ============================================================

def analyze_pricelist_with_markup(items, markup_percent=15):
    """
    Функция бизнес-анализа прайс-листа автомобилей.
    
    Параметры:
        items: list of tuples - список вида [(название, цена), ...]
        markup_percent: int/float - процент наценки (по умолчанию 15)
    
    Возвращает: None (выводит отчёт на экран)
    
    Экономические метрики:
        - benchmark (средняя цена) для позиционирования
        - доля премиум-позиций (индикатор портфеля)
        - прогноз прироста выручки (ROI от наценки)
    """
    
    # ===== РАСПАКОВКА ДАННЫХ =====
    # Преобразуем список кортежей в два отдельных кортежа
    # models будет содержать названия, prices — цены
    models, prices = zip(*items)
    
    # ===== 1. БАЗОВЫЕ СТАТИСТИЧЕСКИЕ МЕТРИКИ =====
    # Средняя арифметическая цена — benchmark для сравнения
    avg_price = sum(prices) / len(prices)
    
    # Максимальная и минимальная цена для анализа ценового диапазона
    max_price = max(prices)
    min_price = min(prices)
    
    # Поиск товаров с экстремальными ценами (условие if внутри генератора списка)
    # [0] в конце — берём первый элемент, так как экстремум может быть только один
    premium_model = [models[i] for i in range(len(prices)) if prices[i] == max_price][0]
    budget_model = [models[i] for i in range(len(prices)) if prices[i] == min_price][0]
    
    # ===== 2. ДОЛЯ ПРЕМИУМ-ПОЗИЦИЙ =====
    # Счётчик автомобилей, цена которых выше средней (показатель премиальности портфеля)
    premium_count = 0
    
    # Цикл for для перебора всех цен
    for price in prices:
        if price > avg_price:           # Условие if — сравнение с benchmark'ом
            premium_count += 1          # Инкремент счётчика
    
    # Расчёт процентной доли (для управленческой отчётности)
    premium_share = (premium_count / len(prices)) * 100
    
    # ===== 3. ЭКОНОМИЧЕСКИЙ ЭФФЕКТ ОТ НАЦЕНКИ =====
    # Текущая выручка (при продаже всего портфеля по текущим ценам)
    current_revenue = sum(prices)
    
    # Генерация новых цен с наценкой (списковая сборка — альтернатива циклу for)
    # round(..., 2) — округление до двух знаков (копейки/рубли)
    new_prices = [price * (1 + markup_percent / 100) for price in prices]
    
    # Прогнозная выручка после применения наценки
    projected_revenue = sum(new_prices)
    
    # Абсолютный прирост выручки (в деньгах)
    revenue_growth = projected_revenue - current_revenue
    
    # Относительный прирост выручки (в процентах) — ключевая метрика ROI
    revenue_growth_percent = (revenue_growth / current_revenue) * 100
    
    # ===== 4. ФОРМИРОВАНИЕ ОБНОВЛЁННОГО ПРАЙСА =====
    # Склеиваем названия и новые цены обратно в список кортежей
    updated_pricelist = [(models[i], round(new_prices[i], 2)) for i in range(len(prices))]
    
    # ===== 5. ВЫВОД УПРАВЛЕНЧЕСКОЙ ОТЧЁТНОСТИ =====
    # Символы "=" и "-" создают визуальное разделение разделов отчёта
    
    print("=" * 70)
    print(" АНАЛИЗ ПРАЙС-ЛИСТА | АВТОСАЛОН (бизнес-аналитика)")
    print("=" * 70)
    print(f" Средняя цена (benchmark): {avg_price:,.2f} ₽")
    print(f" Самый дорогой (флагман): {premium_model} — {max_price:,.2f} ₽")
    print(f" Самый доступный (лидер объёма): {budget_model} — {min_price:,.2f} ₽")
    print(f" Доля премиум-позиций (выше среднего): {premium_count} из {len(prices)} ({premium_share:.1f}%)")
    
    print("\n" + "=" * 70)
    print(" ЭКОНОМИЧЕСКИЙ ЭФФЕКТ от наценки +15%:")
    print("-" * 70)
    print(f"Текущая выручка (портфель): {current_revenue:,.2f} ₽")
    print(f"Прогноз выручки после наценки: {projected_revenue:,.2f} ₽")
    print(f"Прирост выручки: +{revenue_growth:,.2f} ₽ ({revenue_growth_percent:.1f}%)")
    
    print("\n ОБНОВЛЁННЫЙ ПРАЙС-ЛИСТ (для отделадаж):")
    print("-" * 70)
    
    # Цикл for для вывода каждой позиции обновлённого прайса
    for model, new_price in updated_pricelist:
        print(f"  • {model}: {new_price:,.2f} ₽")
    
    print("=" * 70)
    print(" Рекомендация: наценка экономически целесообразна (+{:.1f}% к выручке)".format(revenue_growth_percent))
    print("=" * 70)

